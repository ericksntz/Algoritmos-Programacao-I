# Faça um programa que leia 4 notas bimestrais de uma disciplina, calcule e mostre a média aritmética do bimestre.

n1 = int(input("Digite a nota 1 do bimestre: "))
n2 = int(input("Digite a nota 2 do bimestre: "))
n3 = int(input("Digite a nota 3 do bimestre: "))
n4 = int(input("Digite a nota 4 do bimestre: "))

media = (n1+n2+n3+n4)/4

print ("A média das 4 notas do bimestre é: ", media)