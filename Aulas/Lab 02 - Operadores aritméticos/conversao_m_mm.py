"""
Faça um programa em python que converta metros para milímetros (1m = 1000mm). Considere que
o usuário irá entrar com um número inteiro para metro.
"""

metro = int(input("Conversor de Metros para Milimetros\nDigite a unidade em metros para conversão: "))

milimetro = metro * 1000

print (f"{metro}m em milímetros: {milimetro}mm")